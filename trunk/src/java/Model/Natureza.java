package Model;

/**
 *
 * @author Tiago Peres
 */
public class Natureza {
    private int id;
    private Grupo grupo;
    private String nome;

    public Natureza(int id, String nome){
        this.id = id;
        this.nome = nome;
    }

    public Natureza(){
    }

     /**
     * @author Tiago Peres
     * @version 3.0
     * @since 3.0
     * @return id da natureza
     */
    public int getId() {
        return id;
    }

   /**
     * Muda o id das naturezas a serem exibidas.
     * 
     * @author Tiago Peres
     * @version 3.0
     * @since 3.0
     * @param id das naturezas a serem exibidas
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @author Tiago Peres
     * @version 3.0
     * @since 3.0
     * @return o nome das naturezas a serem exibidas
     */
    public String getNome() {
        return nome;
    }

    /**
     * Muda o nome das naturezas a serem exibidas.
     * 
     * @author Tiago Peres
     * @version 3.0
     * @since 3.0
     * @param nome das naturezas a serem exibidas
     */
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    /**
     * @author Tiago Peres
     * @version 3.0
     * @since 3.0
     * @return nome da natureza como uma string
     */
    @Override
    public String toString(){
        return nome;
    }

    /**
     * @author Tiago Peres
     * @version 3.0
     * @since 3.0
     * @return grupo da natureza a ser exibida
     */
    public Grupo getGrupo() {
        return grupo;
    }

   /**
     * Muda o grupo das naturezas a serem exibidas.
     * 
     * @author Tiago Peres
     * @version 3.0
     * @since 3.0
     * @param grupo das naturezas a serem exibidas
     */
    public void setGrupo(Grupo grupo) {
        this.grupo = grupo;
    }

}
